import java.util.*;
class work9{
	public static void main(String args[]){
		int i = 0;
		System.out.println("Start Do-While");
		do{
			System.out.println(i);
		}while ((i++)<= 10);
		System.out.println("End Do-While");
		
		i = 0;
		
		System.out.println("Start While");
		while((++i)<=10){
			System.out.println(i);
		}
		System.out.println("End While");
	}
}	


	



